//useFetch is a custom React hook that simplifies data fetching from an API. 
// Instead of writing useEffect and fetch repeatedly, you can use useFetch to handle API requests efficiently.


import { useState, useEffect } from "react";

const useFetch = (url) => {
  const [data, setData] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  useEffect(() => {
    const fetchData = async () => {
      setLoading(true);
      try {
        const response = await fetch(url);
        if (!response.ok) throw new Error("Failed to fetch data");
        const result = await response.json();
        setData(result);
      } catch (err) {
        setError(err.message);
      } finally {
        setLoading(false);
      }
    };

    fetchData();
  }, [url]); // Runs when URL changes

  return { data, loading, error };
};

export default useFetch;
